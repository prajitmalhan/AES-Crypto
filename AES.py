# 3 steps: Rotate left, look up in s-box, look up in RCon box
def key_expansion_aux(inp, iter) :
    # Rotate left:
    temp = inp[0]
    for i in range(2) :
        inp[i] = inp[i+1]
    inp[3] = temp

    # Look up in s-box:
    for i in range(3) :
        inp[i] = sbox[inp[i]]

    # Look up in RCon table
    # First byte needs to be XOR'd to itself and the RCon value
    inp[0] ^= RCon[iter]

